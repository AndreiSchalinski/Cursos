//Classe Math para trabalhar com matemática

let numero = 10.234525;

console.log(Math.floor(numero));//função que arredonda pra baixo
console.log(Math.ceil(numero));//função que arredonda pra cima
console.log(Math.round(numero));//função que arredonda se baseando no valor, para ser pra cima ou baixo
console.log(Math.min(12,4,7,1,23,9,32));//função que pega o menor valor da lista
console.log(Math.max(12,4,7,1,23,9,32));//função que pega o maior valor da lista
console.log(Math.PI);//função que pega o valor pi
console.log(Math.pow(2,3));//função que trabalha com potenciação